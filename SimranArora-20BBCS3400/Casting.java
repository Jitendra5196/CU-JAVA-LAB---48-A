package com.apnacollege;

import java.util.Arrays;

public class Casting {

    public static void main(String[] args) {
        //casting
        double price = 100.00;
        double finalPrice = price + 18;
        System.out.println(finalPrice);
        int p = 100;
        int fP = p + (int)18.98;
        System.out.println(fP);
    }

}
